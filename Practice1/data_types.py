s = "Hello World"          # str
print(s)

i = 20                    # int
print(i)

f = 20.5                  # float
print(f)

c = 1j                    # complex
print(c)

l = ["apple", "banana", "cherry"]   # list
print(l)

t = ("apple", "banana", "cherry")   # tuple
print(t)

r = range(6)              # range
print(list(r))  # чтобы было видно значения

d = {"name": "John", "age": 36}     # dict
print(d)

se = {"apple", "banana", "cherry"}  # set
print(se)

frs = frozenset({"apple", "banana", "cherry"})  # frozenset
print(frs)

bo = True                 # bool
print(bo)

by = b"Hello"             # bytes
print(by)

bya = bytearray(5)        # bytearray
print(bya)

m = memoryview(bytes(5))  # memoryview
print(m)

none = None               # NoneType
print(none)

